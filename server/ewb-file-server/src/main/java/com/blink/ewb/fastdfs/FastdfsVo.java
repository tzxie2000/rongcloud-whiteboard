package com.blink.ewb.fastdfs;

public class FastdfsVo {

	private String fileName;

	private String fileExt;

	private long fileLength;

	private String remoteFileGroup;

	private String remoteFileName;

	private String remoteFileExt;

	private String remoteFileUrl;

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getFileExt() {
		return fileExt;
	}

	public void setFileExt(String fileExt) {
		this.fileExt = fileExt;
	}

	public long getFileLength() {
		return fileLength;
	}

	public void setFileLength(long fileLength) {
		this.fileLength = fileLength;
	}

	public String getRemoteFileGroup() {
		return remoteFileGroup;
	}

	public void setRemoteFileGroup(String remoteFileGroup) {
		this.remoteFileGroup = remoteFileGroup;
	}

	public String getRemoteFileName() {
		return remoteFileName;
	}

	public void setRemoteFileName(String remoteFileName) {
		this.remoteFileName = remoteFileName;
	}

	public String getRemoteFileExt() {
		return remoteFileExt;
	}

	public void setRemoteFileExt(String remoteFileExt) {
		this.remoteFileExt = remoteFileExt;
	}

	public String getRemoteFileUrl() {
		return remoteFileUrl;
	}

	public void setRemoteFileUrl(String remoteFileUrl) {
		this.remoteFileUrl = remoteFileUrl;
	}

}
